import turtle as t
t.setup(900,600,0,0)
t.bgcolor('red')
t.color('yellow')
#大五角星
t.pu()
t.goto(-300,240)
t.seth(252)
t.begin_fill()
t.pd()
for i in range(5):
    t.fd(180)
    t.left(144)
t.end_fill()
